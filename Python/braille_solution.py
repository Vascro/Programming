braille_map = {
     't': '011110',
     'h': '110010',
     'e': '100010',
     ' ': '000000',
     'q': '111110',
     'u': '101001',
     'i': '010100',
     'c': '100100',
     'k': '101000',
     'b': '110000',
     'r': '111010',
     'o': '101010',
     'w': '010111',
     'n': '101110',
     'f': '110100',
     'x': '101101',
     'j': '010110',
     'm': '101100',
     'p': '111100',
     's': '011100',
     'v': '111001',
     'l': '111000',
     'a': '100000',
     'z': '101011',
     'y': '101111',
     'd': '100110',
     'g': '110110'}
     
def solution(s):
    braille = ""
    for c in s:
        braille += "000001" if c.isupper() else ''
        braille += braille_map[c.lower()]
    return braille
print(solution(input("")))